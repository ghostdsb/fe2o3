pub fn msf(code: &str) -> String {
  let mut buf = String::new();
  let mut mem = 0_u8;
  for needle in code.chars() {
    match needle {
      '+' => mem = mem.wrapping_add(1),
      '.' => buf.push(mem as char),
      _ => {}
    }
  }
  buf
}

